package org.vaadin.izin.samples.backend.data;

public class Kullanici {
	
	private Long id;
	private String isim;
	private String soyisim;
	private String username;
	private String password;
	private String unvan;
	private String adres;
	private String telefon;
	private String sicil;
	private int devredenIzin;
	private int hakedenIzin;
	private int toplamIzin;
	private int durum;
	
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getIsim() {
		return isim;
	}
	public void setIsim(String isim) {
		this.isim = isim;
	}
	public String getSoyisim() {
		return soyisim;
	}
	public void setSoyisim(String soyisim) {
		this.soyisim = soyisim;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getUnvan() {
		return unvan;
	}
	public void setUnvan(String unvan) {
		this.unvan = unvan;
	}
	public String getAdres() {
		return adres;
	}
	public void setAdres(String adres) {
		this.adres = adres;
	}
	public String getTelefon() {
		return telefon;
	}
	public void setTelefon(String telefon) {
		this.telefon = telefon;
	}
	public String getSicil() {
		return sicil;
	}
	public void setSicil(String sicil) {
		this.sicil = sicil;
	}
	public int getDevredenIzin() {
		return devredenIzin;
	}
	public void setDevredenIzin(int devredenIzin) {
		this.devredenIzin = devredenIzin;
	}
	public int getHakedenIzin() {
		return hakedenIzin;
	}
	public void setHakedenIzin(int hakedenIzin) {
		this.hakedenIzin = hakedenIzin;
	}
	public int getToplamIzin() {
		return toplamIzin;
	}
	public void setToplamIzin(int toplamIzin) {
		this.toplamIzin = toplamIzin;
	}
	public int getDurum() {
		return durum;
	}
	public void setDurum(int durum) {
		this.durum = durum;
	}
	
	

}
